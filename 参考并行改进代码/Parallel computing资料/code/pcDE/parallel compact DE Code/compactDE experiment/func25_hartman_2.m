function fit = func25_hartman_2(x)
fit=0;
a=[10,0.05,3,17;
   3,10,3.5,8;
   17,17,1.7,0.05;
   3.5,0.1,10,10;
   1.7,8,17,0.1;
   8,14,8,14;];
p=[0.1312,0.2329,0.2348,0.4047;
   0.1696,0.4135,0.1415,0.8828;
   0.5569,0.8307,0.3522,0.8732;
   0.0124,0.3736,0.2883,0.5743;
   0.8283,0.1004,0.3047,0.1091;
   0.5886,0.9991,0.6650,0.0381];
a=a';
p=p';
c=[1,1.2,3,3.2];
for i=1:4
    m=0;
    for j=1:6
       m=m+a(i,j)*(x(j)-p(i,j))^2;
    end
    fit=fit+c(i)*exp(-m);
end
fit=-fit;
end

